#Perimeter of a Rectangle - 
# Take length and width as input and calculate the perimeter.
#formula is 2(lenght + widht)

lenght = int(input("Enter lenght of the rectangle in cm: "))
width = int(input("Enter width of the rectangle in cm: "))

p = 2*(lenght + width)

print("The perimeter of the rectangle is: ", p, "cm")