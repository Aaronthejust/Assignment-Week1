# Sum of First N Natural Numbers
# Input a number n, calculate sum of first n natural numbers.
# Formula: sum = n * (n + 1) / 2

n = int(input("Enter the first N Natural Numbers to find its sum: "))
sum = (n * (n + 1))/2
print("The sum for the given first N Natural Numbers is: ", int(sum))